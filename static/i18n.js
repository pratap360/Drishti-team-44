  const LS_KEY = 'kumbh_lang';
  let _lang = localStorage.getItem(LS_KEY) || 'en';
  if (!TRANSLATIONS[_lang]) _lang = 'en';

  /* ----------------------------------------------------------
     Public API
  ---------------------------------------------------------- */

  /**
   * Get the current language code.
   * @returns {'en'|'hi'|'mr'}
   */
  function getLang() {
    return _lang;
  }

  /**
   * Switch the active language and persist to localStorage.
   * Fires a 'langchange' event on window so portals can re-render.
   * @param {'en'|'hi'|'mr'} code
   */
  function setLang(code) {
    if (!TRANSLATIONS[code]) return;
    _lang = code;
    localStorage.setItem(LS_KEY, code);
    window.dispatchEvent(new CustomEvent('langchange', { detail: { lang: code } }));
  }

  /**
   * Translate a key in the current language.
   * Falls back to English, then to the key itself if missing in both.
   * @param {string} key
   * @returns {string}
   */
  function t(key) {
    return (TRANSLATIONS[_lang] && TRANSLATIONS[_lang][key] !== undefined)
      ? TRANSLATIONS[_lang][key]
      : (TRANSLATIONS.en[key] !== undefined ? TRANSLATIONS.en[key] : key);
  }

