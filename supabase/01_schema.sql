-- profiles: a signed-in user can read profiles (role lookup), update own row
drop policy if exists p_profiles_sel on public.profiles;
create policy p_profiles_sel on public.profiles for select to authenticated using (true);
drop policy if exists p_profiles_upd on public.profiles;
create policy p_profiles_upd on public.profiles for update to authenticated using (id = auth.uid());

-- missing_persons: public read; authenticated write (report-found matching/admin updates)
drop policy if exists p_mp_sel on public.missing_persons;
create policy p_mp_sel on public.missing_persons for select to anon, authenticated using (true);
drop policy if exists p_mp_ins on public.missing_persons;
create policy p_mp_ins on public.missing_persons for insert to authenticated with check (true);
drop policy if exists p_mp_upd on public.missing_persons;
create policy p_mp_upd on public.missing_persons for update to authenticated using (true);

-- found_persons: public read; authenticated insert/update
drop policy if exists p_fp_sel on public.found_persons;
create policy p_fp_sel on public.found_persons for select to anon, authenticated using (true);
drop policy if exists p_fp_ins on public.found_persons;
create policy p_fp_ins on public.found_persons for insert to authenticated with check (true);
drop policy if exists p_fp_upd on public.found_persons;
create policy p_fp_upd on public.found_persons for update to authenticated using (true);

-- report_missing: authenticated read + insert (family/admin)
drop policy if exists p_rm_sel on public.report_missing;
create policy p_rm_sel on public.report_missing for select to authenticated using (true);
drop policy if exists p_rm_ins on public.report_missing;
create policy p_rm_ins on public.report_missing for insert to authenticated with check (true);

-- callback_requests: anyone may request
drop policy if exists p_cb_ins on public.callback_requests;
create policy p_cb_ins on public.callback_requests for insert to anon, authenticated with check (true);

-- reference geo tables: public read
