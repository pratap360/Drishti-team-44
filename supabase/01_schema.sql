-- ─── Roles / profiles ───────────────────────────────────────────────────────
-- NOTE: the profiles table already exists in this project with columns
-- (id uuid, role <enum: admin|volunteer|pre_registree>, full_name text,
-- created_at timestamptz). We keep that shape; the "family" portal maps to the
-- pre_registree role. This block is a no-op when the table already exists.
create table if not exists public.profiles (
  id         uuid primary key references auth.users(id) on delete cascade,
  full_name  text,
  role       text not null default 'pre_registree',
  created_at timestamptz default now()
);

-- Auto-create a profile row on new auth user.
create or replace function public.handle_new_user()
returns trigger language plpgsql security definer set search_path = public as $$
begin
  insert into public.profiles (id, full_name, role)
  values (new.id, coalesce(new.raw_user_meta_data->>'full_name', split_part(new.email, '@', 1)),
          coalesce((new.raw_user_meta_data->>'role')::text, 'pre_registree'))
  on conflict (id) do nothing;
  return new;
end $$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

